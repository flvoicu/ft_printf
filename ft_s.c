#include "ft_printf.h"

void	ft_s(char *s, int *cnt)
{
	char	*p;

	p = s;
	if (!p)
		p = "(null)";
	while (*p)
	{
		write(1, p, 1);
		p++;
		*cnt += 1;
	}
}
