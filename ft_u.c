#include "ft_printf.h"

void	ft_u(unsigned int i, int *cnt)
{
	if (i == 0)
	{
		write(1, "0", 1);
		*cnt += 1;
	}
	if (i != 0)
	{
		if (i / 10 != 0)
			ft_u(i / 10, cnt);
		*cnt += 1;
		write(1, &(char){i % 10 + '0'}, 1);
	}
}
