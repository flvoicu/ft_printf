#include "ft_printf.h"

void	ft_p(size_t p, int *cnt)
{
	char	*hex;

	hex = "0123456789abcdef";
	if (p == 0)
	{
		write (1, &hex[p % 16], 1);
		*cnt += 1;
	}
	if (p)
	{
		if (p / 16 != 0)
			ft_p(p / 16, cnt);
		*cnt += 1;
		write (1, &hex[p % 16], 1);
	}
}
